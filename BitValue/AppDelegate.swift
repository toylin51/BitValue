    func application(_ application: UIApplication, didFinishLaunchingWithOptions launchOptions: [UIApplicationLaunchOptionsKey: Any]?) -> Bool {
        // Override point for customization after application launch.
        //改變狀態欄顏色
        UIApplication.shared.statusBarStyle = .lightContent

        //複製 plist
        let path : NSString = Bundle.main.path(forResource: "VirtualCurrency", ofType:  "plist")! as NSString
        let fileManager = FileManager.default
        let paths : NSArray = NSSearchPathForDirectoriesInDomains(.documentDirectory, .userDomainMask, true) as NSArray
        let documentsDirectory = paths.object(at: 0) as! NSString
        let writablePath = documentsDirectory.appendingPathComponent("VirtualCurrency.plist")
        if !fileManager.fileExists(atPath: writablePath){
            do{
                try fileManager.copyItem(atPath: path as String, toPath: writablePath)
            }catch{
                print(error)
            }
        }
        
        return true
    }

